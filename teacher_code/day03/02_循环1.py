# 循环实现上边的效果

i = 0 # 定义循环条件需要的变量i
while i <= 100: # 能够循环继的条件
    print(f'这是第{i}次')
    print('我错了！') # 循环每一次都要干的事情
    # 每次循环的时候，让 i+1
    i += 1 # 改变了循环的条件

# 循环结束了才向下执行
print('等循环结束了，我才执行')


